package week3;

public class Car {

	public String brand;
	public short year;
	String color;
	byte speed;
	short tax;
	
	
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public short getYear() {
		return year;
	}
	public void setYear(short year) {
		this.year = year;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public byte getSpeed() {
		return speed;
	}
	public void setSpeed(byte speed) {
		this.speed = speed;
	}
	public short getTax() {
		return tax;
	}
	public void setTax(short tax) {
		this.tax = tax;
	}
	

		

	
	
	
	
	
	
	
}
